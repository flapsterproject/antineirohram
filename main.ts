serve(async (req: Request) => {
  const { pathname } = new URL(req.url);
  if (pathname !== SECRET_PATH) return new Response("ok");
  if (req.method !== "POST") return new Response("Method Not Allowed", { status: 405 });

  const update = await req.json();
  const msg = update.message;
  const chatId = msg?.chat?.id;
  const messageId = msg?.message_id;
  const userId = msg?.from?.id;
  const text = msg?.text;
  const username = msg?.from?.username;
  const isBot = msg?.from?.is_bot;

  if (!chatId || !text || !userId) return new Response("ok");

  if (isBot) return new Response("ok"); // –¢–æ–ª—å–∫–æ –æ—Ç–≤–µ—á–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º, –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º –±–æ—Ç–æ–≤

  const sessionKey = `${chatId}:${userId}`;

  // --- –ö–æ–º–∞–Ω–¥–∞ /math ---
  if (text.toLowerCase().startsWith("/math")) {
    mathSessions[sessionKey] = true;
    await sendMessage(chatId, "–†–µ–∂–∏–º –º–∞—Ç–µ–º–∞—Ç–∏–∫–∏ –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω! –ù–∞–ø–∏—à–∏ –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ (–Ω–∞–ø—Ä–∏–º–µ—Ä, 2+2*3), –∏ —è —Ä–µ—à—É –µ–≥–æ —à–∞–≥ –∑–∞ —à–∞–≥–æ–º, –µ—Å–ª–∏ –Ω—É–∂–Ω–æ. –î–ª—è –≤—ã—Ö–æ–¥–∞ –ø—Ä–æ—Å—Ç–æ –Ω–∞–ø–∏—à–∏ —á—Ç–æ-—Ç–æ –Ω–µ–º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ.", messageId);
    return new Response("ok");
  }

  // --- –ú–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∞—è —Å–µ—Å—Å–∏—è ---
  if (mathSessions[sessionKey]) {
    const solution = solveMath(text);
    await sendMessage(chatId, solution, messageId);
    mathSessions[sessionKey] = false; // –≤—ã–∫–ª—é—á–∞–µ–º —Å–µ—Å—Å–∏—é
    return new Response("ok");
  }

  // –û—Ç–≤–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –∏–ª–∏ —Å–æ–∑–¥–∞—Ç–µ–ª—é
  let replyText: string;
  if (username === CREATOR_USERNAME) {
    const footballReply = analyzeFootballMessage(text, username);
    replyText = footballReply ? footballReply : analyzeCreatorMessage(text);
  } else {
    const footballReply = analyzeFootballMessage(text, username);
    replyText = footballReply || analyzeMessage(text);
  }

  await sendMessage(chatId, replyText, messageId);

  return new Response("ok");
});